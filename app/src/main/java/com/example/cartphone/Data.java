package com.example.cartphone;

public class Data {

    private String productIDStr;

    private String productName;

    private float productPrice;

    private String productQuantity;

    private String productSubTotal;


    public String getProductIDStr() {
        return productIDStr;
    }

    public void setProductIDStr(String productIDStr) {
        this.productIDStr = productIDStr;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public float getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(float productPrice) {
        this.productPrice = productPrice;
    }

    public String getProductQuantity() {
        return productQuantity;
    }

    public void setProductQuantity(String productQuantity) {
        this.productQuantity = productQuantity;
    }

    public String getProductSubTotal() {
        return productSubTotal;
    }

    public void setProductSubTotal(String productSubTotal) {
        this.productSubTotal = productSubTotal;
    }
}
